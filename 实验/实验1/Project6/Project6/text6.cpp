#include <iostream>
using namespace std;
int main()
{
	cout << "char lengh:" << sizeof(char) << endl;
	cout << "int lengh:" << sizeof(int) << endl;
	cout << "unsigned int lengh:" << sizeof(unsigned int) << endl;
	cout << "short lengh:" << sizeof(short) << endl;
	cout << "unsigned short lengh:" << sizeof(unsigned short) << endl;
	cout << "long lengh:" << sizeof(long) << endl;
	cout << "unsigned long lengh:" << sizeof(unsigned long) << endl;
	cout << "long long lengh:" << sizeof(long long) << endl;
	cout << "float lengh:" << sizeof(float) << endl;
	cout << "double lengh:" << sizeof(double) << endl;
	cout << "bool lengh:" << sizeof(bool) << endl;
	return 0;
}